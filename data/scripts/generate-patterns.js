#!/usr/bin/env node

/**
 * Generate individual pattern files from mcp-server/patterns.json.
 * Creates one JS file per real pattern (where pattern !== null) in data/patterns/
 * and a _index.js that imports and re-exports all of them.
 *
 * Usage: node data/scripts/generate-patterns.js
 */

import { readFileSync, writeFileSync, mkdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const patternsDir = join(__dirname, '..', 'patterns')
const sourceFile = join(__dirname, '..', '..', 'mcp-server', 'patterns.json')

// Ensure output directory exists
mkdirSync(patternsDir, { recursive: true })

// Load source data
const allEntries = JSON.parse(readFileSync(sourceFile, 'utf-8'))
const realPatterns = allEntries.filter(p => p.pattern !== null)

console.log(`Found ${realPatterns.length} real patterns out of ${allEntries.length} entries.`)

function slugify(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}

function cleanText(text) {
  if (!text) return ''
  // Keep full text but clean up whitespace and trim to reasonable length
  return text.replace(/\n{3,}/g, '\n\n').trim()
}

const generated = []

for (const p of realPatterns) {
  const num = String(p.pattern).padStart(2, '0')
  const slug = slugify(p.name)
  const filename = `${num}-${slug}.js`

  const content = `// Pattern ${p.pattern}: ${p.name}
// Part: ${p.part}

export default {
  id: 'pattern-${p.pattern}',
  number: ${p.pattern},
  name: ${JSON.stringify(p.name)},
  slug: ${JSON.stringify(slug)},
  part: ${JSON.stringify(p.part)},
  problem: ${JSON.stringify(cleanText(p.problem))},
  solution: ${JSON.stringify(cleanText(p.solution))},
  keywords: ${JSON.stringify(p.keywords.slice(0, 20))},
}
`

  writeFileSync(join(patternsDir, filename), content)
  generated.push({ number: p.pattern, slug, filename })
}

// Generate _index.js
const imports = generated
  .sort((a, b) => a.number - b.number)
  .map(g => `import p${g.number} from './${g.filename}'`)
  .join('\n')

const patternList = generated
  .sort((a, b) => a.number - b.number)
  .map(g => `  p${g.number},`)
  .join('\n')

const indexContent = `// Auto-generated index of ${generated.length} patterns
// Generated by: node data/scripts/generate-patterns.js

${imports}

export const patterns = [
${patternList}
]

export const patternsByNumber = Object.fromEntries(
  patterns.map(p => [p.number, p])
)

export const patternsBySlug = Object.fromEntries(
  patterns.map(p => [p.slug, p])
)
`

writeFileSync(join(patternsDir, '_index.js'), indexContent)

console.log(`Generated ${generated.length} pattern files + _index.js in data/patterns/`)
