#!/usr/bin/env node

/**
 * Generate individual principle and UX pattern files from mcp-server/design-principles.json.
 * Creates:
 *   - data/principles/NN-slug.js (17 files) + _index.js
 *   - data/ux-patterns/pN-slug.js (7 files) + _index.js
 *
 * Usage: node data/scripts/generate-principles.js
 */

import { readFileSync, writeFileSync, mkdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const principlesDir = join(__dirname, '..', 'principles')
const uxPatternsDir = join(__dirname, '..', 'ux-patterns')
const sourceFile = join(__dirname, '..', '..', 'mcp-server', 'design-principles.json')

// Ensure output directories exist
mkdirSync(principlesDir, { recursive: true })
mkdirSync(uxPatternsDir, { recursive: true })

// Load source data
const allEntries = JSON.parse(readFileSync(sourceFile, 'utf-8'))

function slugify(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}

// ── Generate Principles ──

const principles = allEntries.filter(e => e.type === 'principle' && e.number != null)
console.log(`Found ${principles.length} principles.`)

const generatedPrinciples = []

for (const p of principles) {
  const num = String(p.number).padStart(2, '0')
  const slug = slugify(p.name)
  const filename = `${num}-${slug}.js`

  const content = `// Principle ${p.number}: ${p.name}
// Theme: ${p.theme}

export default {
  id: 'principle-${p.number}',
  number: ${p.number},
  name: ${JSON.stringify(p.name)},
  slug: ${JSON.stringify(slug)},
  theme: ${JSON.stringify(p.theme)},
  summary: ${JSON.stringify(p.summary || '')},
  keywords: ${JSON.stringify(p.keywords.slice(0, 20))},
}
`

  writeFileSync(join(principlesDir, filename), content)
  generatedPrinciples.push({ number: p.number, slug, filename })
}

// Generate _index.js for principles
const pImports = generatedPrinciples
  .sort((a, b) => a.number - b.number)
  .map(g => `import pr${g.number} from './${g.filename}'`)
  .join('\n')

const pList = generatedPrinciples
  .sort((a, b) => a.number - b.number)
  .map(g => `  pr${g.number},`)
  .join('\n')

const principlesIndex = `// Auto-generated index of ${generatedPrinciples.length} principles
// Generated by: node data/scripts/generate-principles.js

${pImports}

export const principles = [
${pList}
]

export const principlesByNumber = Object.fromEntries(
  principles.map(p => [p.number, p])
)

export const principlesBySlug = Object.fromEntries(
  principles.map(p => [p.slug, p])
)
`

writeFileSync(join(principlesDir, '_index.js'), principlesIndex)
console.log(`Generated ${generatedPrinciples.length} principle files + _index.js in data/principles/`)

// ── Generate UX Patterns ──

const uxPatterns = allEntries.filter(e => e.type === 'ux-pattern' && e.number != null)
console.log(`Found ${uxPatterns.length} UX patterns.`)

const uxPatternCodes = {
  1: 'P1',
  2: 'P2',
  3: 'P3',
  4: 'P4',
  5: 'P5',
  6: 'P6',
  7: 'P7',
}

// Extract lifecycle phase from the name/content
const uxLifecyclePhases = {
  1: 'Pre-Action',
  2: 'Pre-Action',
  3: 'In-Action',
  4: 'In-Action',
  5: 'Post-Action',
  6: 'Post-Action',
  7: 'Post-Action',
}

const generatedUx = []

for (const p of uxPatterns) {
  const slug = slugify(p.name)
  const code = uxPatternCodes[p.number] || `P${p.number}`
  const phase = uxLifecyclePhases[p.number] || 'Unknown'
  const filename = `p${p.number}-${slug}.js`

  const content = `// UX Pattern ${code}: ${p.name}
// Lifecycle Phase: ${phase}

export default {
  id: 'ux-pattern-${p.number}',
  number: ${p.number},
  code: ${JSON.stringify(code)},
  name: ${JSON.stringify(p.name)},
  slug: ${JSON.stringify(slug)},
  lifecyclePhase: ${JSON.stringify(phase)},
  summary: ${JSON.stringify(p.summary || '')},
  keywords: ${JSON.stringify(p.keywords.slice(0, 20))},
}
`

  writeFileSync(join(uxPatternsDir, filename), content)
  generatedUx.push({ number: p.number, code, slug, filename })
}

// Generate _index.js for UX patterns
const uImports = generatedUx
  .sort((a, b) => a.number - b.number)
  .map(g => `import ux${g.number} from './${g.filename}'`)
  .join('\n')

const uList = generatedUx
  .sort((a, b) => a.number - b.number)
  .map(g => `  ux${g.number},`)
  .join('\n')

const uxIndex = `// Auto-generated index of ${generatedUx.length} UX patterns
// Generated by: node data/scripts/generate-principles.js

${uImports}

export const uxPatterns = [
${uList}
]

export const uxPatternsByNumber = Object.fromEntries(
  uxPatterns.map(p => [p.number, p])
)

export const uxPatternsBySlug = Object.fromEntries(
  uxPatterns.map(p => [p.slug, p])
)
`

writeFileSync(join(uxPatternsDir, '_index.js'), uxIndex)
console.log(`Generated ${generatedUx.length} UX pattern files + _index.js in data/ux-patterns/`)
